  <legend>Purpose and Basics of GitHub</legend>

  <article id="tutorials-about-github">
    <h4>GitHub on the Command Line (<a href="http://gitref.org/basic/#add" target="_blank">Good resource here</a>)</h4>

    <div class="row">
      <div class="col-xs-4 col-sm-4 col-md-4 col-lg-4">
        <p><code>$ git pull</code></p>
      </div>
      <div class="col-xs-1 xol-sm-1 col-md-1 col-lg-1">
				<i class="fa fa-long-arrow-right"></i>
			</div>
      <div class="col-xs-7 col-sm-7 col-md-7 col-lg-7">
        <p>Pulls down bookmark history and incorporates the changes into your repository.</p>
      </div>
    </div>

    <div class="row">
      <div class="col-xs-4 col-sm-4 col-md-4 col-lg-4">
        <p><code>$ git status</code></p>
      </div>
      <div class="col-xs-1 xol-sm-1 col-md-1 col-lg-1">
				<i class="fa fa-long-arrow-right"></i>
			</div>
      <div class="col-xs-7 col-sm-7 col-md-7 col-lg-7">
        <p>Lists all new or modified files to be commited.</p>
      </div>
    </div>

    <div class="row">
      <div class="col-xs-4 col-sm-4 col-md-4 col-lg-4">
        <p><code>$ git add [file]</code></p>
      </div>
      <div class="col-xs-1 xol-sm-1 col-md-1 col-lg-1">
				<i class="fa fa-long-arrow-right"></i>
			</div>
      <div class="col-xs-7 col-sm-7 col-md-7 col-lg-7">
        <p>Adds the files in the local repository and stages them for commit.</p>
      </div>
    </div>

    <div class="row">
      <div class="col-xs-4 col-sm-4 col-md-4 col-lg-4">
        <p><code>$ git commit -m ""</code></p>
      </div>
      <div class="col-xs-1 xol-sm-1 col-md-1 col-lg-1">
        <i class="fa fa-long-arrow-right"></i>
      </div>
      <div class="col-xs-7 col-sm-7 col-md-7 col-lg-7">
        <p>Commits the tracked changes and prepares them to be pushed to a remote repository.</p>
      </div>
    </div>

    <div class="row">
      <div class="col-xs-4 col-sm-4 col-md-4 col-lg-4">
        <p><code>$ git push</code></p>
      </div>
      <div class="col-xs-1 xol-sm-1 col-md-1 col-lg-1">
				<i class="fa fa-long-arrow-right"></i>
			</div>
      <div class="col-xs-7 col-sm-7 col-md-7 col-lg-7">
        <p>Pushes the changes in your local repository up to the remote repository you specified as the origin.</p>
      </div>
    </div>

    <div class="row">
      <div class="col-xs-4 col-sm-4 col-md-4 col-lg-4">
        <p><code>$ git diff</code></p>
      </div>
      <div class="col-xs-1 xol-sm-1 col-md-1 col-lg-1">
				<i class="fa fa-long-arrow-right"></i>
			</div>
      <div class="col-xs-7 col-sm-7 col-md-7 col-lg-7">
        <p>Shows file differences not yet staged</p>
      </div>
    </div>

    <div class="row">
      <div class="col-xs-4 col-sm-4 col-md-4 col-lg-4">
        <p><code>$ git reset [file]</code></p>
      </div>
      <div class="col-xs-1 xol-sm-1 col-md-1 col-lg-1">
				<i class="fa fa-long-arrow-right"></i>
			</div>
      <div class="col-xs-7 col-sm-7 col-md-7 col-lg-7">
        <p>Unstages the file, but preserve its contents.</p>
      </div>
    </div>

    <div class="row">
      <div class="col-xs-4 col-sm-4 col-md-4 col-lg-4">
        <p><code>$ git checkout</code></p>
      </div>
      <div class="col-xs-1 xol-sm-1 col-md-1 col-lg-1">
				<i class="fa fa-long-arrow-right"></i>
			</div>
      <div class="col-xs-7 col-sm-7 col-md-7 col-lg-7">
        <p>Switches to the specified branch and updates the working directory.</p>
      </div>
    </div>

